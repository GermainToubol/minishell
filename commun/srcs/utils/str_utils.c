#include <stdlib.h>

int	last_char(char *s, char c)
{
	size_t	i;
	int		r;

	i = 0;
	r = -1;
	while (s[i])
	{
		if (s[i] == c)
			r = (int)i;
		i++;
	}
	return (r);
}
