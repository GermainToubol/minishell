int	c_separator(char c)
{
	if (c == ' ' || c == '\t' || c == '|' || c == '&' || c == '>' || c == '<'
		|| c == '\0' || c == '(' || c == ')' || c == '"' || c == '\''
		|| c == '\n')
		return (1);
	return (0);
}
